CREATE DOMAIN banner_type AS TEXT CHECK (VALUE ~ '^(https?://)(?:www\.)?[a-zA-Z0-9\-\.]{1,255}(?:/[a-zA-Z0-9\-\._~:\/?#\[\]@!$&''()*+,;=]{0,255})?$');
