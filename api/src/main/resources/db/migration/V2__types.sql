CREATE DOMAIN art_type AS TEXT CHECK (VALUE ~ '^RESPIRATION|KEKKIJUTSU|FIGHTING_STYLE$');
CREATE DOMAIN npc_type AS TEXT CHECK (VALUE ~ 'HUMAN|ONI|HYBRID');
